document.addEventListener("DOMContentLoaded", () => {
    const botonesAgentes = document.querySelectorAll(".btn-abrir-modal");
    const modalElemento = document.getElementById('agenteModal');
    
    // Verificamos que el modal exista para evitar errores
    if (modalElemento) {
        const modalAgente = new bootstrap.Modal(modalElemento);

        botonesAgentes.forEach(boton => {
            boton.addEventListener("click", function() {
                const idAgente = this.getAttribute("data-id-agente");
                const info = BD_agentes[idAgente]; // Usamos BD_agentes

                if (info) {
                    document.getElementById("modalNombre").textContent = info.nombre;
                    // Asegúrate de que las propiedades coincidan con tu objeto BD_agentes
                    // Nota: En tu archivo tenías 'especificacion', cámbialo a 'rol' si prefieres
                    document.getElementById("modalAtributo").textContent = `Atributo: ${info.atributo}`;
                    document.getElementById("modalRol").textContent = `Rol: ${info.especificacion}`;
                    document.getElementById("modalImagen").src = info.imagen || "";
                    document.getElementById("modalDescripcion").textContent = info.descripcion;
                    document.getElementById("modalBuildF2P").textContent = info.buildF2P;
                    document.getElementById("modalSinergia").textContent = info.sinergias;

                    modalAgente.show();
                }
            });
        });
    }
});